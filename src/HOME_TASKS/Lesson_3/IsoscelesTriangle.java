package HOME_TASKS.Lesson_3;

public class IsoscelesTriangle {

  public static void main(String[] args) {
    int a = 1;
    int b = 3;
    int c = 3;

    if (a == b || a == c || b == c) {
      System.out.println("This is the isosceles triangle");
    } else {
      System.out.println("This is not the isosceles triangle");
    }
  }
}
